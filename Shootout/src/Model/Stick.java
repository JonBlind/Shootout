package Model;


/**
 * This class represents the stick that every Player holds. All Players possess a stick that can
 * be extended to initiate a poke check, which dislodges the puck from another Player's possession.
 * This forces the puck to start traveling in the direction/angle the poker was.
 */
public class Stick {
  private double length;
  private double width;
  private double angle;
  private double extended;
  private double Position;
}
